    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
       
            if(txtUser.getText().length()==0)
            {
                JOptionPane.showConfirmDialog(this, "Enter Your UserName");
            }
            else if (txtPass.getText().length()==0)
                    {
                        JOptionPane.showConfirmDialog(this, "Please Enter your Password");
    }
            else if (txtPass.getText().equals(txtConfirm.getText())==false){
                JOptionPane.showConfirmDialog(this, "Password Didn't Match");
            }
            else{
                try {
                    String 	username=txtUser.getText();
                    String password=txtPass.getText();
                    String utype=txtUtype.getSelectedItem().toString();
                            
                    Class.forName("com.mysql.jdbc.Driver");
                    con=DriverManager.getConnection("jdbc:mysql://localhost/sms","root","");
                    pst=con.prepareStatement("insert into newuser (username,password,utype) values(?,?,?) ");
                    
                    pst.setString(1, username);
                    pst.setString(2, password);
                    pst.setString(3, utype);
                    pst.executeUpdate();
                    JOptionPane.showConfirmDialog(null, "User Created");
                    txtUser.setText("");
                     txtPass.setText("");
                   txtConfirm.setText("");
                   txtUtype.setSelectedIndex(-1);
                   txtUser.requestFocus();
                    
                    
                } catch (ClassNotFoundException ex) {
                    Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
                } catch (SQLException ex) {
                    Logger.getLogger(NewUser.class.getName()).log(Level.SEVERE, null, ex);
                }
            }

// TODO add your handling code here:
    }//GEN-LAST:event_jButton1ActionPerformed

